function setLang(lang){
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key = el.getAttribute("data-i18n");
    if(i18n[lang] && i18n[lang][key]) el.textContent = i18n[lang][key];
  });
  document.querySelectorAll(".chip").forEach(b=>{
    b.classList.toggle("chip--active", b.dataset.lang === lang);
  });
  document.documentElement.lang = lang === "ka" ? "ka" : "en";
  localStorage.setItem("abi_lang", lang);
}

const yearEl = document.getElementById("year");
if(yearEl) yearEl.textContent = new Date().getFullYear();

document.querySelectorAll(".chip").forEach(btn=>{
  btn.addEventListener("click", ()=> setLang(btn.dataset.lang));
});

setLang(localStorage.getItem("abi_lang") || "en");

// mobile menu
const ham = document.getElementById("hamburger");
const mobileNav = document.getElementById("mobileNav");
if(ham && mobileNav){
  ham.addEventListener("click", ()=>{
    const isHidden = mobileNav.hasAttribute("hidden");
    if(isHidden) mobileNav.removeAttribute("hidden");
    else mobileNav.setAttribute("hidden", "");
  });
}
